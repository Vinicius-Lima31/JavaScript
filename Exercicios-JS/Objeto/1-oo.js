// Principions Importantes
/* 1 . Abstração :
*   é você pegar um objeto da vida real e tentar traduzir esse objeto para o programra
*   por exemplo o carro, que tem metodo volante, ligar carro, macha etc */

/* 2. Encapsulamento:
*   é voce ter os detalhes de implementação escondidos, e mostrar pra quem precisa usar, mostrar interface simples
*   que você pode interagir com diversos objeto
*   Carro por exemplo, voce tem que aprender ligar, mexer com macha.
*   Mais como a gasolina queimada e tals, como o mecanismo da marcha, muitas informações de encapsalmento esta vendo,
*   nivel de interface e importante que tenha conhecimento
*   quando voce muda implementação interna, não necessariamente o usuario tem que se acostumar
*   apenas se mudar a interface
*   Muito importante, se mudar implementação não necessariamente precisara se adaptar, apenas se mudar interface que pessoal tera que se adptar */

/* 3. Herança (prototype) :
*   é um
*   Herança que recebe atributos e tals de um objeto PAI
*   O carro tem porta, a porta tem parafusos, etc..
*   Em JS HERANÇA Tem apenas 1 par  */

/* 4. Polimorfismo: 
*   Significa multiplas formas, é o fato que voce por exemplo tem um celular, pode ser iphone, moto g
*   Através do polimorfismo, voce aponta um carro, e uma hora esse carro pode apontar para uma ferrari
*   um uno, ou um camaro, polimorfismo quer dizer multiplas formas, mais é um principio mais para linguagens tipadas

*/

